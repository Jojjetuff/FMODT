	function stringsplit(input, seperator)
		if seperator == nil then
			seperator = "%s"
		end
		
		local t={} ; i=1
		
		for str in string.gmatch(input, "([^"..seperator.."]+)") do
			t[i] = str
			i = i + 1
		end
		
		return t
	end

